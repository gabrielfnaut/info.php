<?php

// + - * /

$value = "4";
$value2 = "4";
$operation = "+";
$result = 0 ;

if ($operation == "+") {  
    $result = $value + $value2 ; 
} 

if ($operation == "-") {
    $result = $value - $value2 ;
}

if ($operation == "/") {
    $result = $value / $value2 ;
}

if ($operation == "*") {
    $result = $value * $value2 ; 
}

if ($operation == "**" ) {
    $result = $value ** $value2 ;
}

if ($operation == "%" ) {
    $result = $value % $value2 ;
}

